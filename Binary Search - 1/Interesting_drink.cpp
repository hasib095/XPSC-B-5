#include <bits/stdc++.h>
using namespace std;

#define ll long long
#define endl "\n"
#define all(x) begin(x), end(x)

void solve()
{
    ll n;
    cin >> n;
    vector<ll> v(n);
    for (ll i = 0; i < n; i++)
    {
        cin >> v[i];
    }
    sort(all(v));
    ll q;
    cin >> q;
    for (ll i = 0; i < q; i++)
    {
        ll val;
        cin >> val;
        auto up = upper_bound(all(v), val);
        ll ans = up - v.begin();
        cout << ans << endl;
    }
}
